#include "main.h"

/**
 * print_diagonal - the function print n times \ to draw a diagonal line
 * @n: the number of times it has to be printed
 * return: no return as it is void
 */
void print_diagonal(int n)
{
	int i;
	int j;

	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		for (i = 0; i < n; i++)
		{
			for (j = 0; j < i; j++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			_putchar('\n');
		}
	}
}
